#Exemplo fatiamento simples
frase = "Aluno Iure da Jala"
print(frase[9])

#Exemplo fatiameto composto
frase = "Aluno Iure da Jala"
print(frase[0:5])


#Exemplo fatiamento composto com pulo
frase = "Aluno Iure da Jala"
print(frase[0:5:2])

#Exemplo fatiamento com apenas o fim definido
frase = "Aluno Iure da Jala"
print(frase[:5])

#Exemplo fatiamento com apenas inico definido
frase = "Aluno Iure da Jala"
print(frase[5:])

#Exemplo fatiamento como inicio definido e pulo
frase = "Aluno Iure da Jala"
print(frase[5::3])

