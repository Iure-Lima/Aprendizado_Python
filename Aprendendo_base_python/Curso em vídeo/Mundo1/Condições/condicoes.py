# Exemplo condiçoes simples
numero = 4

if numero == 5:
    print("O número é 5")

# Exemplo condições composta
numero = 3

if numero == 5:
    print("O número é 5")
else:
    print("O número não é 5")

# Exemplo condições ternarias
numero = 5

print("O número é 5" if numero == 5 else "O número não é 5")
